# 구구단(중첩 for 반복문)

i, k = 0, 0 # 초기화

for i in range(2, 10, 1):
    for k in range(1, 10, 1):
        print("%d * %d = %2d"%(i, k, i*k))

    print("-" * 10)

# f" {} " : f-string 문자열 포맷 함수(python3 이후 최신)
# format 함수
# %d :decimal : 숫자(정) %s : string (python2)

# %d : 문자열에서 정수 값 삽입

age = 20
txt = "나는 %d살입니다."%age

print(txt)

# %s : s(str) 문자열에서 문자 값을 삽입

name = "Dr.Will"
txt = "My name is %s"%name
print(txt)

# %2d : 정수값의 너비 지정

number = 7
formatted_num = "%4d" % number
print(formatted_num)
# 출력이 앞에 공백 추가 2문자 너비 구성됨
# 7로 출력되고 앞의 공백 1개 있음

number = 27
