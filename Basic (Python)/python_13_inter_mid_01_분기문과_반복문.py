for i in range(1,101):
    print("for 반복문을 %d번 실행합니다"%i)

# break 문 (정지조건)

for i in range(1,101):
    print("for 반복문을 %d번 실행합니다"%i)
    break

# continue문 (~제외)

i, sum = 0, 0 # 초기화

for i in range(1,101):
    if i % 3 == 0:  #3의 배수이면
        continue # 제외 >> 건너 뛰어

    sum += i # sum = sum + i

    # 실행과정 확인
    # print(i)
    # print(sum)

# 최종 결과 (3의 배수 제외한 1~10까지 합계)
